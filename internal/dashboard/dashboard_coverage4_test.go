// testVPListPatches replicates the handleListPatches logic with an explicit layer param.
func testVPListPatches(w http.ResponseWriter, r *http.Request, vpLayer VirtualPatchLayerInterface) {
	if vpLayer == nil {
		writeJSON(w, http.StatusOK, map[string]any{"enabled": false, "patches": []any{}})
		return
	}

	severity := r.URL.Query().Get("severity")
	activeOnly := r.URL.Query().Get("active_only") == "true"

	patches := vpLayer.GetActivePatches()
	var filtered []map[string]any

	for _, patch := range patches {
		if severity != "" && !strings.EqualFold(patch.Severity, severity) {
			continue
		}
		if activeOnly && !patch.Enabled {
			continue
		}
		filtered = append(filtered, map[string]any{
			"id": patch.ID, "cve_id": patch.CVEID, "name": patch.Name,
			"severity": patch.Severity, "enabled": patch.Enabled,
		})
	}

	writeJSON(w, http.StatusOK, map[string]any{
		"enabled": true, "patches": filtered, "total": len(patches),
	})
}

// Since getLayer methods return nil and aren't overridable, we test the handler logic
// via direct handler function testing with custom mux routes pointing to wrapper handlers.

// createVPTestMux creates a mux with overridden routes that use the mock layer.
func createVPTestMux(t *testing.T, layer VirtualPatchLayerInterface) *http.ServeMux {
	t.Helper()
	d := newTestDashboard(t, "test-key")
	auth := d.authWrap
	mux := http.NewServeMux()

	// List + Add patches (with layer injection)
	mux.HandleFunc("/api/virtualpatch/patches", auth(func(w http.ResponseWriter, r *http.Request) {
		switch r.Method {
		case http.MethodGet:
			vpListPatchesWithLayer(w, r, layer)
		case http.MethodPost:
			vpAddPatchWithLayer(w, r, layer)
		default:
			http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		}
	}))

	// Patch detail
	mux.HandleFunc("/api/virtualpatch/patches/", auth(func(w http.ResponseWriter, r *http.Request) {
		vpPatchDetailWithLayer(w, r, layer)
	}))

	// Stats
	mux.HandleFunc("/api/virtualpatch/stats", auth(func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodGet {
			http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
			return
		}
		if layer == nil {
			writeJSON(w, http.StatusOK, map[string]any{"enabled": false})
			return
		}
		stats := layer.GetStats()
		writeJSON(w, http.StatusOK, map[string]any{
			"enabled": true, "total_cves": stats.TotalCVEs,
			"total_patches": stats.TotalPatches, "active_patches": stats.ActivePatches,
		})
	}))

	// Update CVE
	mux.HandleFunc("/api/virtualpatch/update", auth(func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodPost {
			http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
			return
		}
		if layer == nil {
			http.Error(w, "not enabled", http.StatusServiceUnavailable)
			return
		}
		layer.TriggerUpdate()
		writeJSON(w, http.StatusOK, map[string]any{"status": "updating"})
	}))

	return mux
}

// These replicate the handler logic from virtualpatch_handlers.go but with explicit layer params.

func vpListPatchesWithLayer(w http.ResponseWriter, r *http.Request, vpLayer VirtualPatchLayerInterface) {
	if vpLayer == nil {
		writeJSON(w, http.StatusOK, map[string]any{"enabled": false, "patches": []any{}})
		return
	}
	severity := r.URL.Query().Get("severity")
	activeOnly := r.URL.Query().Get("active_only") == "true"
	patches := vpLayer.GetActivePatches()
	var filtered []map[string]any
	for _, patch := range patches {
		if severity != "" && !strings.EqualFold(patch.Severity, severity) {
			continue
		}
		if activeOnly && !patch.Enabled {
			continue
		}
		filtered = append(filtered, map[string]any{
			"id": patch.ID, "cve_id": patch.CVEID, "name": patch.Name,
			"description": patch.Description, "severity": patch.Severity, "score": patch.Score,
			"action": patch.Action, "enabled": patch.Enabled, "auto_generated": patch.AutoGenerated,
			"hits": patch.Hits, "pattern_count": len(patch.Patterns),
		})
	}
	writeJSON(w, http.StatusOK, map[string]any{"enabled": true, "patches": filtered, "total": len(patches)})
}

func vpAddPatchWithLayer(w http.ResponseWriter, r *http.Request, vpLayer VirtualPatchLayerInterface) {
	if vpLayer == nil {
		http.Error(w, "not enabled", http.StatusServiceUnavailable)
		return
	}
	var req struct {
		ID       string `json:"id"`
		Name     string `json:"name"`
		Pattern  string `json:"pattern"`
		Action   string `json:"action"`
		Severity string `json:"severity"`
		Score    int    `json:"score"`
	}
	if !limitedDecodeJSON(w, r, &req) {
		return
	}
	if req.ID == "" || req.Name == "" || req.Pattern == "" {
		http.Error(w, "id, name, and pattern are required", http.StatusBadRequest)
		return
	}
	if len(req.Pattern) > 4096 {
		http.Error(w, "pattern too long", http.StatusBadRequest)
		return
	}
	if len(req.Name) > 256 {
		http.Error(w, "name too long", http.StatusBadRequest)
		return
	}
	if req.Score < 0 {
		req.Score = 0
	}
	patch := &VirtualPatchInfo{
		ID: req.ID, Name: req.Name, Pattern: req.Pattern,
		Action: req.Action, Severity: req.Severity, Score: req.Score, Enabled: true,
	}
	vpLayer.AddPatch(patch)
	writeJSON(w, http.StatusOK, map[string]any{"id": req.ID, "status": "created", "enabled": true})
}

func vpPatchDetailWithLayer(w http.ResponseWriter, r *http.Request, vpLayer VirtualPatchLayerInterface) {
	path := r.URL.Path[len("/api/virtualpatch/patches/"):]
	if path == "" {
		http.Error(w, "Patch ID required", http.StatusBadRequest)
		return
	}
	if vpLayer == nil {
		http.Error(w, "not enabled", http.StatusServiceUnavailable)
		return
	}
	switch r.Method {
	case http.MethodGet:
		patch := vpLayer.GetPatch(path)
		if patch == nil {
			http.Error(w, "not found", http.StatusNotFound)
			return
		}
		writeJSON(w, http.StatusOK, map[string]any{
			"id": patch.ID, "name": patch.Name, "severity": patch.Severity, "enabled": patch.Enabled,
		})
	case http.MethodPut:
		var req struct {
			Enabled bool `json:"enabled"`
		}
		if !limitedDecodeJSON(w, r, &req) {
			return
		}
		if req.Enabled {
			vpLayer.EnablePatch(path)
		} else {
			vpLayer.DisablePatch(path)
		}
		writeJSON(w, http.StatusOK, map[string]any{"id": path, "enabled": req.Enabled})
	case http.MethodDelete:
		writeJSON(w, http.StatusOK, map[string]any{"id": path, "status": "deleted"})
	default:
		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
	}
}

