func TestLoginRateLimit_LockoutExpiry(t *testing.T) {
	d := newTestDashboard(t, "secret")
	ip := "192.0.2.99"

	for range loginMaxAttempts {
		d.recordLoginFailure(ip)
	}

	if d.checkLoginRateLimit(ip) {
		t.Error("expected IP to be locked out")
	}

	// Set lockout to expired
	val, ok := d.loginBuckets.Load(ip)
	if !ok {
		t.Fatal("expected bucket to exist")
	}
	b := val.(*loginBucket)
	b.mu.Lock()
	b.lockUntil = time.Now().Add(-time.Minute)
	b.mu.Unlock()

	if !d.checkLoginRateLimit(ip) {
		t.Error("expected IP to be allowed after lockout expiry")
	}
}

// =====================================================================
// Cleanup login buckets with expired and non-expired entries
// =====================================================================

func TestCleanupLoginBuckets_ExpiredAndActive(t *testing.T) {
	d := newTestDashboard(t, "secret")

	// Expired locked bucket
	val1, _ := d.loginBuckets.LoadOrStore("ip-expired-locked", &loginBucket{})
	b1 := val1.(*loginBucket)
	b1.mu.Lock()
	b1.locked = true
	b1.lockUntil = time.Now().Add(-time.Minute)
	b1.mu.Unlock()

	// Expired non-locked bucket (old failure)
	val2, _ := d.loginBuckets.LoadOrStore("ip-expired-old", &loginBucket{})
	b2 := val2.(*loginBucket)
	b2.mu.Lock()
	b2.attempts = 3
	b2.lastFail = time.Now().Add(-loginWindow - time.Hour)
	b2.mu.Unlock()

	// Active bucket (should NOT be removed)
	val3, _ := d.loginBuckets.LoadOrStore("ip-active", &loginBucket{})
	b3 := val3.(*loginBucket)
	b3.mu.Lock()
	b3.attempts = 1
	b3.lastFail = time.Now()
	b3.mu.Unlock()

	// Simulate cleanup logic
	now := time.Now()
	d.loginBuckets.Range(func(key, value any) bool {
		b := value.(*loginBucket)
		b.mu.Lock()
		expired := b.locked && now.After(b.lockUntil) || (!b.locked && now.Sub(b.lastFail) > loginWindow)
		b.mu.Unlock()
		if expired {
			d.loginBuckets.Delete(key)
		}
		return true
	})

	if _, ok := d.loginBuckets.Load("ip-expired-locked"); ok {
		t.Error("expected expired locked bucket to be removed")
	}
	if _, ok := d.loginBuckets.Load("ip-expired-old"); ok {
		t.Error("expected expired old bucket to be removed")
	}
	if _, ok := d.loginBuckets.Load("ip-active"); !ok {
		t.Error("expected active bucket to remain")
	}
}

